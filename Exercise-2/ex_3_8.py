place_list=['Canada','USA','New Zealand','UK','Australia']
print('Original List')
print(place_list)
place_list.sort()
print('\nSorted List')
print(place_list)
place_list.reverse()
print('\nReversed List')
print(place_list)
place_list.reverse()
print('\nAgain Reversed List')
print(place_list)
place_list.sort()
print('\nAlphabetical Order')
print(place_list)
place_list.sort(reverse=True)
print('\nRevere Alphabetical Order')
print(place_list)